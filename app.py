@st.cache_data
def find_results_file():
    """Automatically find the results CSV file from multiple locations."""
    possible_names = [
        'tshf_nids_results.csv',
        'results.csv',
        'model_results.csv',
        'performance_results.csv',
        'experiment_results.csv',
        'final_summary.csv',
        'summary.csv'
    ]
    
    search_paths = [
        '.',
        './data',
        './results',
        './output',
        '/kaggle/working',
        '/kaggle/input',
        '../',
        '../data',
        '../results'
    ]
    
    found_files = []
    for path in search_paths:
        for name in possible_names:
            full_path = os.path.join(path, name)
            if os.path.exists(full_path):
                found_files.append(full_path)
        
        if os.path.exists(path):
            for file in glob.glob(os.path.join(path, '*results*.csv')):
                found_files.append(file)
            for file in glob.glob(os.path.join(path, '*Results*.csv')):
                found_files.append(file)
    
    found_files = list(set(found_files))
    
    if found_files:
        return found_files[0]
    return None

@st.cache_data
def load_results_data(file_path=None):
    """Load results from CSV file with smart column detection."""
    if file_path is None:
        file_path = find_results_file()
    
    if file_path is None:
        st.warning("⚠️ No results CSV found. Using sample data.")
        return load_sample_data(), None
    
    try:
        df = pd.read_csv(file_path)
        
        # Show what we loaded
        st.sidebar.markdown(f"**📄 File:** {os.path.basename(file_path)}")
        st.sidebar.markdown(f"**📊 Rows:** {len(df)}")
        st.sidebar.markdown(f"**📋 Columns:** {len(df.columns)}")
        
        # Apply smart column detection
        df = smart_column_detection(df)
        
        st.success(f"✅ Loaded results from: {file_path}")
        return df, file_path
        
    except Exception as e:
        st.error(f"❌ Error loading file: {e}")
        return load_sample_data(), None

