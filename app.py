def plot_model_comparison(df):
    """Create interactive model comparison chart."""
    fig = make_subplots(
        rows=1, cols=2,
        subplot_titles=('🎯 Accuracy Comparison', '🏆 F1-Score Comparison'),
        specs=[[{'type': 'bar'}, {'type': 'bar'}]]
    )
    
    datasets = df['Dataset'].tolist()
    
    # Accuracy plot
    fig.add_trace(
        go.Bar(name='CNN-LSTM-Attention', x=datasets, y=df['Deep_Accuracy'],
               marker_color='#667eea', text=df['Deep_Accuracy'].round(4),
               textposition='outside', texttemplate='%{text:.2%}'),
        row=1, col=1
    )
    fig.add_trace(
        go.Bar(name='Random Forest', x=datasets, y=df['RF_Accuracy'],
               marker_color='#f093fb', text=df['RF_Accuracy'].round(4),
               textposition='outside', texttemplate='%{text:.2%}'),
        row=1, col=1
    )
    fig.add_trace(
        go.Bar(name='XGBoost', x=datasets, y=df['XGB_Accuracy'],
               marker_color='#4facfe', text=df['XGB_Accuracy'].round(4),
               textposition='outside', texttemplate='%{text:.2%}'),
        row=1, col=1
    )
    
    # F1 plot
    fig.add_trace(
        go.Bar(name='CNN-LSTM-Attention', x=datasets, y=df['Deep_F1'],
               marker_color='#667eea', text=df['Deep_F1'].round(4),
               textposition='outside', texttemplate='%{text:.2%}', showlegend=False),
        row=1, col=2
    )
    fig.add_trace(
        go.Bar(name='Random Forest', x=datasets, y=df['RF_F1'],
               marker_color='#f093fb', text=df['RF_F1'].round(4),
               textposition='outside', texttemplate='%{text:.2%}', showlegend=False),
        row=1, col=2
    )
    fig.add_trace(
        go.Bar(name='XGBoost', x=datasets, y=df['XGB_F1'],
               marker_color='#4facfe', text=df['XGB_F1'].round(4),
               textposition='outside', texttemplate='%{text:.2%}', showlegend=False),
        row=1, col=2
    )
    
    fig.update_layout(
        height=450,
        showlegend=True,
        legend=dict(
            orientation="h",
            yanchor="bottom",
            y=1.02,
            xanchor="right",
            x=1
        ),
        template='plotly_white',
        font=dict(family="Arial, sans-serif", size=12),
        bargap=0.15
    )
    
    fig.update_yaxes(range=[0.92, 1.0], tickformat='.1%', row=1, col=1)
    fig.update_yaxes(range=[0.92, 1.0], tickformat='.1%', row=1, col=2)
    
    return fig

def plot_radar_comparison(df):
    """Create radar chart for model comparison."""
    categories = ['Accuracy', 'Precision', 'Recall', 'F1-Score']
    
    fig = go.Figure()
    
    colors = ['#667eea', '#f093fb', '#4facfe']
    
    for idx, row in df.iterrows():
        values = [
            row['Deep_Accuracy'],
            row['Deep_Precision'],
            row['Deep_Recall'],
            row['Deep_F1']
        ]
        fig.add_trace(go.Scatterpolar(
            r=values,
            theta=categories,
            fill='toself',
            name=row['Dataset'],
            line=dict(color=colors[idx % len(colors)], width=2),
            marker=dict(size=8)
        ))
    
    fig.update_layout(
        polar=dict(
            radialaxis=dict(
                visible=True,
                range=[0.92, 1.0],
                tickformat='.1%'
            )
        ),
        showlegend=True,
        height=450,
        template='plotly_white',
        font=dict(family="Arial, sans-serif", size=12)
    )
    
    return fig

def plot_improvement_chart(df):
    """Create improvement chart showing gains over baselines."""
    fig = go.Figure()
    
    datasets = df['Dataset'].tolist()
    
    fig.add_trace(go.Bar(
        name='vs Random Forest',
        x=datasets,
        y=(df['Deep_Accuracy'] - df['RF_Accuracy']) * 100,
        marker_color='#11998e',
        text=(df['Deep_Accuracy'] - df['RF_Accuracy']) * 100,
        textposition='outside',
        texttemplate='+%{text:.2f}%'
    ))
    
    fig.add_trace(go.Bar(
        name='vs XGBoost',
        x=datasets,
        y=(df['Deep_Accuracy'] - df['XGB_Accuracy']) * 100,
        marker_color='#f5576c',
        text=(df['Deep_Accuracy'] - df['XGB_Accuracy']) * 100,
        textposition='outside',
        texttemplate='+%{text:.2f}%'
    ))
    
    fig.update_layout(
        title='📈 Improvement Over Baseline Models',
        height=350,
        barmode='group',
        yaxis_title='Improvement (%)',
        template='plotly_white',
        font=dict(family="Arial, sans-serif", size=12),
        legend=dict(orientation="h", yanchor="bottom", y=1.02, xanchor="center", x=0.5)
    )
    
    return fig

